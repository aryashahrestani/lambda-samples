# مرتب‌سازی لیست با لامبدا (key function)
# لیستی از تاپل‌های (نام، سن) تعریف می‌کنیم
students = [("Ali", 18), ("Sara", 20), ("Reza", 15)]
# مرتب‌سازی بر اساس سن (دومین عنصر تاپل)
# لیست را بر اساس سن (دومین عنصر) با استفاده از لامبدا مرتب می‌کنیم
sorted_students = sorted(students, key=lambda s: s[1])
# نتیجه مرتب‌سازی را چاپ می‌کنیم
print(sorted_students)  # [('Reza',15), ('Ali',18), ('Sara',20)]
