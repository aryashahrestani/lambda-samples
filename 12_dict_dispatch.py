# استفاده از لامبدا در دیکشنری برای توزیع عملیات‌های مختلف
# لامبدا درون دیکشنری (dispatch)
# یک دیکشنری شامل چهار عمل ریاضی با توابع لامبدا تعریف می‌کنیم
operations = {
    # تابع لامبدا برای جمع دو عدد
    "add": lambda a, b: a + b,
    # تابع لامبدا برای تفریق دو عدد
    "sub": lambda a, b: a - b,
    # تابع لامبدا برای ضرب دو عدد
    "mul": lambda a, b: a * b,
    # تابع لامبدا برای تقسیم دو عدد (با بررسی تقسیم بر صفر)
    "div": lambda a, b: a / b if b != 0 else "error",
}

# عملیات جمع را با مقادیر ۱۰ و ۵ صدا می‌زنیم و نتیجه را چاپ می‌کنیم
print(operations["add"](10, 5))   # 15
# عملیات تقسیم را با مقادیر ۱۰ و ۰ صدا می‌زنیم (خطای تقسیم بر صفر مدیریت می‌شود)
print(operations["div"](10, 0))   # error
