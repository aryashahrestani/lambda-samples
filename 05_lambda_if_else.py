# لامبدا با if-else (عملگر سه‌تایی)
# تابع لامبدا که بزرگترین عدد بین دو ورودی را برمی‌گرداند
max_value = lambda a, b: a if a > b else b
# تابع را با اعداد 10 و 20 تست می‌کنیم
print(max_value(10, 20))  # 20

# بررسی زوج یا فرد
# تابع لامبدا که زوج یا فرد بودن عدد را بررسی می‌کند
parity = lambda n: "even" if n % 2 == 0 else "odd"
# تابع را با عدد 7 تست می‌کنیم
print(parity(7))  # odd
