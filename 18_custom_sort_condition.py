# مرتب‌سازی با شرط سفارشی در پارامتر key
# sort با شرط سفارشی در key
# یک لیست از کلمات مختلف تعریف می‌کنیم
words = ["hello", "world", "hi", "python", "a", "code"]

# مرتب‌سازی عجیب: اول بر اساس زوج بودن طول، سپس طول
# یک تابع معمولی برای کلید مرتب‌سازی تعریف می‌کنیم
def custom_key(w):
    # یک تاپل برمی‌گرداند: اول زوج بودن طول، سپس خود طول
    return (len(w) % 2 == 0, len(w))

# تبدیل به لامبدا:
# کلمات را با لامبدا مرتب می‌کنیم: اول طول فرد/زوج، سپس طول کلمه
sorted_words = sorted(words, key=lambda w: (len(w) % 2 == 0, len(w)))
# نتیجه مرتب‌شده را چاپ می‌کنیم
print(sorted_words)
# ['a', 'hi', 'code', 'hello', 'world', 'python']
# اول طول‌های فرد (a, hi, code) سپس زوج (hello, world, python)
