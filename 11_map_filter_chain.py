# این فایل ترکیب توابع map و filter را با لامبدا نشان می‌دهد
# ترکیب map + filter در یک خط
# یک لیست از اعداد ۱ تا ۶ تعریف می‌کنیم
nums = [1, 2, 3, 4, 5, 6]
# ابتدا اعداد زوج فیلتر می‌شوند و سپس هر کدام دو برابر می‌شوند
# اول filter (اعداد زوج) بعد map (دو برابر)
# از filter برای انتخاب اعداد زوج و map برای دو برابر کردن آنها استفاده می‌کنیم
result = list(map(lambda x: x * 2, filter(lambda x: x % 2 == 0, nums)))
# نتیجه نهایی را چاپ می‌کنیم
print(result)  # [4, 8, 12]
