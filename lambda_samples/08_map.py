# map: اعمال تابع به همه عناصر
# لیستی از اعداد تعریف می‌کنیم
nums = [1, 2, 3, 4]
# با استفاده از map و لامبدا، مربع هر عدد را محاسبه می‌کنیم
squared = list(map(lambda x: x ** 2, nums))
# نتیجه را چاپ می‌کنیم
print(squared)  # [1, 4, 9, 16]

# map با دو لیست
# دو لیست اعداد تعریف می‌کنیم
a = [1, 2, 3]
b = [4, 5, 6]
# با استفاده از map، عناصر متناظر دو لیست را جمع می‌زنیم
sums = list(map(lambda x, y: x + y, a, b))
# نتیجه را چاپ می‌کنیم
print(sums)  # [5, 7, 9]
