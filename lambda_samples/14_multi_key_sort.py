# مرتب‌سازی پیشرفته با استفاده از چند کلید در لامبدا
# مرتب‌سازی پیشرفته با چند کلید
# یک لیست از تاپل‌های حاوی نام، سن و نمره تعریف می‌کنیم
data = [
    ("Ali", 25, 80),
    ("Sara", 22, 90),
    ("Reza", 25, 75),
    ("Mina", 22, 85),
]

# اول بر اساس سن، سپس بر اساس نمره (نزولی)
# داده‌ها را مرتب می‌کنیم: اول سن به صورت صعودی، سپس نمره به صورت نزولی
sorted_data = sorted(data, key=lambda x: (x[1], -x[2]))
# نتیجه مرتب‌شده را چاپ می‌کنیم
print(sorted_data)
# [('Mina',22,85), ('Sara',22,90), ('Reza',25,75), ('Ali',25,80)]
