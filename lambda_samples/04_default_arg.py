# لامبدا با مقدار پیش‌فرض
# تابع لامبدا برای توان رساندن با یک پارامتر پیش‌فرض (exp=2)
power = lambda base, exp=2: base ** exp
# تابع را با یک آرگومان صدا می‌زنیم (از توان پیش‌فرض 2 استفاده می‌کند)
print(power(5))     # 25  (5^2)
# تابع را با دو آرگومان صدا می‌زنیم (توان 3)
print(power(2, 3))  # 8   (2^3)
